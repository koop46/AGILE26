import scipy.stats as stats


class KirkpatrickModel:
    
    def __init__(self, pre_test_data: dict[str, list[int]], post_test_data: dict[str, list[int]]):
        self.pre_test_data = pre_test_data
        self.post_test_data = post_test_data

    def p_value(self) -> float:
        n_of_users = len(self.pre_test_data)
        t_stat = self.t_stat()
        df = n_of_users - 1
        t_dist = stats.t(df=df)
        cdf_val = t_dist.cdf(abs(t_stat))
        return 2 * (1 - cdf_val)

    def t_stat(self) -> float:
        mean_diff = self.mean_diff()
        std_diff = self.std_diff()
        n_of_users = len(self.pre_test_data)
        t_stat = mean_diff / (std_diff / (n_of_users ** 0.5))
        return t_stat

    def cohens_d(self) -> float:
        mean_diff = self.mean_diff()
        std_diff = self.std_diff()
        return mean_diff / std_diff

    def std_diff(self) -> float:
        diff_vector = self.total_score_differences()
        mean_diff = self.mean_diff()
        sum_sq = sum((diff - mean_diff) ** 2 for diff in diff_vector)
        std = (sum_sq / (len(diff_vector) - 1)) ** 0.5
        return std

    def mean_diff(self) -> float:
        avg_vectors = self.total_score_differences()
        return sum(avg_vectors) / len(avg_vectors)

    def total_score_differences(self) -> list[float]:
        diff_vectors = []
        for key in self.pre_test_data:
            diff = 0
            for i in range(len(self.pre_test_data[key])):
                diff += (self.post_test_data[key][i] - self.pre_test_data[key][i])
            diff_vectors.append(diff)
        return diff_vectors

    def summary(self):
        n = len(self.pre_test_data)
        mean_diff = self.mean_diff()
        std_diff = self.std_diff()
        t_stat = self.t_stat()
        p_val = self.p_value()
        d = self.cohens_d()
        pre_test_results = sum([score for scores in self.pre_test_data.values() for score in scores])
        post_test_results = sum([score for scores in self.post_test_data.values() for score in scores])
        n_questions = len(list(self.pre_test_data.values())[0])

        # extremly significant (a <0.001), highly significant (a <0.01), significant (a <0.05)

        return {
            "n_users":n,
            "question_per_user": n_questions,
            "pre_test_pct": pre_test_results/(n_questions*n)*100,      # <-- MUST match the Python field name
            "post_test_pct": post_test_results/(n_questions*n)*100,
            "p_value":float(p_val),
            "mean_improvement":mean_diff,
            "std_improvement":std_diff,
            "cohens_d":d,
            "t_stat":t_stat,
        }
 
        



